namespace SudokuStudio.Drawing;

/// <summary>
/// Defines a mode that is used for displaying an assignment.
/// </summary>
public enum AssignmentDisplay
{
	/// <summary>
	/// Indicates the display mode is to draw a solid circle.
	/// </summary>
	CircleSolid,

	/// <summary>
	/// Indicates the display mode is to draw a hollow circle.
	/// </summary>
	CircleHollow
}
