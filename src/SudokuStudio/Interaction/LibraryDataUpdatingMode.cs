namespace SudokuStudio.Interaction;

/// <summary>
/// Represents a mode to display for library data.
/// </summary>
public enum LibraryDataUpdatingMode
{
	/// <summary>
	/// Indicates the mode is adding.
	/// </summary>
	Add,

	/// <summary>
	/// Indicates the mode is loading.
	/// </summary>
	Load,

	/// <summary>
	/// Indicates the mode is updating.
	/// </summary>
	Update,

	/// <summary>
	/// Indicates the mode is adding one puzzle.
	/// </summary>
	AddOne
}
