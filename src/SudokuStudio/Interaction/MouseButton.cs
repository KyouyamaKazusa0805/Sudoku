namespace SudokuStudio.Interaction;

/// <summary>
/// Defines a mouse button.
/// </summary>
public enum MouseButton
{
	/// <summary>
	/// Indicates the mouse button is left button.
	/// </summary>
	Left,

	/// <summary>
	/// Indicates the mouse button is right button.
	/// </summary>
	Right,

	/// <summary>
	/// Indicates the mouse button is middle button (mouse wheel).
	/// </summary>
	Middle
}
